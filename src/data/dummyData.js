// Helper to generate a month's attendance records dynamically for 20 employees
// This avoids code bloat and provides structured data matching the current dates
const generateAttendanceData = () => {
  const records = [];
  const employeesIdList = ['EMP101', 'EMP102', 'EMP103', 'EMP104', 'EMP105', 'EMP106', 'EMP107', 'EMP108', 'EMP109', 'EMP110', 'EMP111', 'EMP112', 'EMP113', 'EMP114', 'EMP115', 'EMP118', 'EMP119', 'EMP120'];
  
  // Pick past 10 working days
  for (let i = 1; i <= 10; i++) {
    const dayStr = i < 10 ? `0${i}` : `${i}`;
    const dateStr = `2026-06-${dayStr}`;
    
    employeesIdList.forEach(empId => {
      // Find matching employee details
      const emp = initialEmployees.find(e => e.id === empId);
      const randomSeed = Math.random();
      
      let checkIn = '09:02 AM';
      let checkOut = '05:45 PM';
      let status = 'On Time';
      let hours = '8.2 hrs';
      
      if (randomSeed < 0.05) {
        checkIn = '09:45 AM';
        checkOut = '05:30 PM';
        status = 'Late';
        hours = '7.2 hrs';
      } else if (randomSeed < 0.08) {
        checkIn = '-';
        checkOut = '-';
        status = 'Absent';
        hours = '0 hrs';
      }

      records.push({
        key: `${empId}-${dateStr}`,
        empId,
        name: emp.fullName,
        date: dateStr,
        checkIn,
        checkOut,
        workingHours: hours,
        totalHours: hours,
        breakHours: '0.8 hrs',
        overtime: '0.2 hrs',
        attendanceStatus: status,
        status: status,
        remarks: 'Direct biometric scanner log'
      });
    });
  }
  return records;
};

